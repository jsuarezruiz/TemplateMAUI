namespace TemplateMAUI.Controls
{
    /// <summary>
    /// The StepStatus enumeration defines the possible statuses for a step within a step-based control. 
    /// It is used to indicate the current state of a step, such as whether it is completed, in progress, or waiting to be started.
    /// </summary>
    public enum StepStatus
    {
        Complete,
        InProgress,
        NotStarted
    }
}
